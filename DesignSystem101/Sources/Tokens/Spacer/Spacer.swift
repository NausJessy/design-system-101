import UIKit

/// An enumeration of the default spacers provided by DesignKit.
public enum Spacer: CGFloat {

    /// A spacer of 5pt.
    case tight = 5.0

    /// A spacer of 10pt.
    case standard = 10.0

    /// A spacer of 15pt.
    case medium = 15.0

    /// A spacer of 20pt.
    case wide = 20.0

    /// A spacer of 25pt.
    case extraWide = 25.0
}
